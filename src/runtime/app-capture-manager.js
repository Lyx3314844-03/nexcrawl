function toArray(value) {
  return Array.isArray(value) ? value : [];
}

function ringPush(items, entry, limit = 200) {
  items.push(entry);
  if (items.length > limit) {
    items.shift();
  }
}

function nowIso() {
  return new Date().toISOString();
}

function parseCommand(command) {
  const parts = String(command ?? '').match(/(?:[^\s"]+|"[^"]*")+/g) ?? [];
  const [bin, ...args] = parts.map((entry) => entry.replace(/^"(.*)"$/, '$1'));
  return {
    command: bin ?? '',
    args,
  };
}

function resolveExecutionSpec(step, appConfig = {}) {
  const override =
    step.id === 'frida-attach'
      ? appConfig.frida?.exec
      : step.id === 'mitmproxy-capture'
        ? appConfig.mitmproxy?.exec
        : null;

  if (override && typeof override === 'object' && override.command) {
    return {
      command: override.command,
      args: Array.isArray(override.args) ? override.args.map((entry) => String(entry)) : [],
      shell: override.shell === true,
    };
  }

  const parsed = parseCommand(step.command);
  return {
    command: parsed.command,
    args: parsed.args,
    shell: false,
  };
}

function resolveFridaTemplateKind(appConfig = {}) {
  const platform = String(appConfig.platform ?? '').trim().toLowerCase();
  if (platform === 'ios') {
    return 'ios';
  }
  if (platform === 'android' || platform === 'webview' || platform === 'miniapp') {
    return 'android';
  }
  return 'generic';
}

function buildAndroidFridaScript(appConfig = {}) {
  const bundleId = appConfig.frida?.bundleId ?? appConfig.bundleId ?? 'target.bundle.id';
  return `// Auto-generated by OmniCrawl AppCaptureManager.
// Android-oriented starter template for common SSL pinning and JS bridge inspection.

setImmediate(function () {
  console.log('[omnicrawl] android frida helper loaded for ${bundleId}');

  if (typeof Java !== 'undefined' && Java.available) {
    Java.perform(function () {
      console.log('[omnicrawl] Java runtime available; applying starter Android hooks.');

      try {
        const CertificatePinner = Java.use('okhttp3.CertificatePinner');
        if (CertificatePinner.check) {
          CertificatePinner.check.overloads.forEach((overload) => {
            overload.implementation = function () {
              console.log('[omnicrawl] bypassed okhttp3.CertificatePinner.check');
              return;
            };
          });
        }
      } catch (_error) {}

      try {
        const HostnameVerifier = Java.use('javax.net.ssl.HostnameVerifier');
        console.log('[omnicrawl] HostnameVerifier available:', !!HostnameVerifier);
      } catch (_error) {}

      try {
        Java.enumerateLoadedClasses({
          onMatch(name) {
            if (/webview|bridge|javascriptinterface/i.test(name)) {
              console.log('[omnicrawl] bridge-like class:', name);
            }
          },
          onComplete() {},
        });
      } catch (_error) {}
    });
    return;
  }

  console.log('[omnicrawl] Java runtime unavailable; attach native hooks manually if required.');
});

rpc.exports = {
  ping() {
    return 'pong';
  },
};
`;
}

function buildIosFridaScript(appConfig = {}) {
  const bundleId = appConfig.frida?.bundleId ?? appConfig.bundleId ?? 'target.bundle.id';
  return `// Auto-generated by OmniCrawl AppCaptureManager.
// iOS-oriented starter template for NSURLSession / trust evaluation inspection.

setImmediate(function () {
  console.log('[omnicrawl] ios frida helper loaded for ${bundleId}');

  if (typeof ObjC !== 'undefined' && ObjC.available) {
    try {
      const NSURLSession = ObjC.classes.NSURLSession;
      if (NSURLSession && NSURLSession['- dataTaskWithRequest:completionHandler:']) {
        const impl = NSURLSession['- dataTaskWithRequest:completionHandler:'].implementation;
        Interceptor.attach(impl, {
          onEnter(args) {
            try {
              const request = new ObjC.Object(args[2]);
              console.log('[omnicrawl] NSURLSession request:', request.URL().absoluteString().toString());
            } catch (_error) {}
          },
        });
      }
    } catch (_error) {}

    try {
      const WKWebView = ObjC.classes.WKWebView;
      console.log('[omnicrawl] WKWebView available:', !!WKWebView);
    } catch (_error) {}

    try {
      const SecTrustEvaluateWithError = Module.findExportByName(null, 'SecTrustEvaluateWithError');
      if (SecTrustEvaluateWithError) {
        Interceptor.attach(SecTrustEvaluateWithError, {
          onLeave(retval) {
            console.log('[omnicrawl] observed SecTrustEvaluateWithError');
          },
        });
      }
    } catch (_error) {}
    return;
  }

  console.log('[omnicrawl] Objective-C runtime unavailable; attach native hooks manually if required.');
});

rpc.exports = {
  ping() {
    return 'pong';
  },
};
`;
}

function buildGenericFridaScript(appConfig = {}) {
  const bundleId = appConfig.frida?.bundleId ?? appConfig.bundleId ?? 'target.bundle.id';
  return `// Auto-generated by OmniCrawl AppCaptureManager.
// Generic fallback template. Replace with platform-specific hooks as needed.

setImmediate(function () {
  console.log('[omnicrawl] generic frida helper loaded for ${bundleId}');

  if (typeof Java !== 'undefined' && Java.available) {
    Java.perform(function () {
      console.log('[omnicrawl] Java runtime available; consider swapping in the Android template.');
    });
    return;
  }

  if (typeof ObjC !== 'undefined' && ObjC.available) {
    console.log('[omnicrawl] Objective-C runtime available; consider swapping in the iOS template.');
    return;
  }

  console.log('[omnicrawl] no managed runtime detected; attach native hooks manually if required.');
});

rpc.exports = {
  ping() {
    return 'pong';
  },
};
`;
}

function buildDefaultFridaScript(appConfig = {}) {
  const kind = resolveFridaTemplateKind(appConfig);
  if (kind === 'ios') {
    return buildIosFridaScript(appConfig);
  }
  if (kind === 'android') {
    return buildAndroidFridaScript(appConfig);
  }
  return buildGenericFridaScript(appConfig);
}

