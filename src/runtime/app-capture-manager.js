function buildBundleReadme({ appConfig = {}, generated = {}, plan = null } = {}) {
  const bundleId = appConfig.frida?.bundleId ?? appConfig.bundleId ?? 'target.bundle.id';
  const lines = [
    '# OmniCrawl Capture Bundle',
    '',
    `Target bundle: ${bundleId}`,
    `Platform: ${appConfig.platform ?? 'unknown'}`,
    '',
    'Recommended order:',
    '1. Review capture-manifest.json.',
    '2. Start mitmdump using android-bootstrap.sh or windows-bootstrap.bat.',
    '3. Configure the device proxy and trust the mitmproxy CA certificate.',
    '4. Attach Frida with the generated helper script.',
    '5. Launch the target app and verify traffic appears in the dump.',
    '',
    'Generated files:',
    ...Object.entries(generated)
      .filter(([, value]) => typeof value === 'string' && value.trim())
      .map(([key, value]) => `- ${key}: ${value}`),
  ];

  if (plan?.steps?.length) {
    lines.push('', 'Planned tools:');
    for (const step of plan.steps) {
      lines.push(`- ${step.id}: ${step.tool} (${step.mode ?? 'manual'})`);
    }
  }

  return `${lines.join('\n')}\n`;
}

function buildCaptureManifest({ sessionId, captureDir, appConfig = {}, generated = {}, plan = null } = {}) {
  const generatedFiles = Object.entries(generated)
    .filter(([, filePath]) => typeof filePath === 'string' && filePath.trim())
    .map(([key, filePath]) => ({
      key,
      path: filePath,
      relativePath:
        captureDir && filePath.startsWith(captureDir)
          ? filePath.slice(captureDir.length).replace(/^[\\/]+/, '')
          : filePath,
    }));

  return {
    sessionId,
    captureDir,
    target: {
      bundleId: appConfig.frida?.bundleId ?? appConfig.bundleId ?? null,
      deviceId: appConfig.frida?.deviceId ?? null,
      platform: appConfig.platform ?? null,
      fridaTemplateKind: resolveFridaTemplateKind(appConfig),
    },
    generatedFiles,
    tools: {
      frida: appConfig.frida?.enabled === true,
      mitmproxy: appConfig.mitmproxy?.enabled === true,
    },
    quickStart: {
      reviewManifest: generated.captureManifestPath ?? null,
      fridaScript: generated.fridaScriptPath ?? null,
      mitmproxyAddon: generated.mitmproxyAddonPath ?? null,
      androidBootstrap: generated.androidBootstrapPath ?? null,
      windowsBootstrap: generated.windowsBootstrapPath ?? null,
      androidProxySetup: generated.androidProxySetupPath ?? null,
      androidClearProxy: generated.androidClearProxyPath ?? null,
    },
    operatorChecklist: [
      'Start mitmdump before launching the target app.',
      'Configure the target device or emulator to use the proxy host/port.',
      'Install and trust the mitmproxy CA certificate if HTTPS interception is required.',
      'Attach Frida only after proxying is ready so early requests are captured.',
      'Use the clear-proxy helper when you want to restore device networking.',
    ],
    plan,
  };
}

function buildCaptureAssetPayload(session = {}) {
  const generated = session.generated && typeof session.generated === 'object' ? session.generated : {};
  return {
    sessionId: session.id,
    status: session.status,
    platform: session.app?.platform ?? null,
    captureDir: session.captureDir ?? null,
    target: {
      bundleId: session.app?.frida?.bundleId ?? session.app?.bundleId ?? null,
      deviceId: session.app?.frida?.deviceId ?? null,
    },
    generatedFiles: Object.entries(generated)
      .filter(([, filePath]) => typeof filePath === 'string' && filePath.trim())
      .map(([key, filePath]) => ({ key, path: filePath })),
    plan: session.plan ?? null,
    processes: (session.processes ?? []).map(({ child, ...entry }) => entry),
    logTail: [...(session.logs ?? [])].slice(-50),
  };
}

function ensureGeneratedCaptureHelpers({ projectRoot, sessionId, app = {} } = {}) {
  const captureDir = resolve(projectRoot, '.omnicrawl', 'app-captures', sessionId);
  mkdirSync(captureDir, { recursive: true });

  const nextApp = structuredClone(app);
  const generated = {};

  if (nextApp.frida?.enabled === true && !nextApp.frida?.scriptPath) {
    const scriptPath = join(captureDir, 'frida-auto.js');
    writeFileSync(scriptPath, buildDefaultFridaScript(nextApp), 'utf8');
    nextApp.frida = {
      ...(nextApp.frida ?? {}),
      scriptPath,
    };
    generated.fridaScriptPath = scriptPath;
  }

  if (nextApp.mitmproxy?.enabled === true) {
    if (!nextApp.mitmproxy?.dumpPath) {
      nextApp.mitmproxy = {
        ...(nextApp.mitmproxy ?? {}),
        dumpPath: join(captureDir, 'traffic.dump'),
      };
      generated.mitmproxyDumpPath = nextApp.mitmproxy.dumpPath;
    }

    if (!nextApp.mitmproxy?.addonPath) {
      const addonPath = join(captureDir, 'mitm-addon.py');
      writeFileSync(addonPath, buildDefaultMitmproxyAddon(), 'utf8');
      nextApp.mitmproxy = {
        ...(nextApp.mitmproxy ?? {}),
        addonPath,
      };
      generated.mitmproxyAddonPath = addonPath;
    }
  }

  const bootstrapPaths = {
    fridaScriptPath: nextApp.frida?.scriptPath,
    mitmproxyAddonPath: nextApp.mitmproxy?.addonPath,
    mitmproxyDumpPath: nextApp.mitmproxy?.dumpPath,
  };

  const androidBootstrapPath = join(captureDir, 'android-bootstrap.sh');
  writeFileSync(androidBootstrapPath, buildAndroidBootstrapScript(nextApp, bootstrapPaths), 'utf8');
  generated.androidBootstrapPath = androidBootstrapPath;

  const windowsBootstrapPath = join(captureDir, 'windows-bootstrap.bat');
  writeFileSync(windowsBootstrapPath, buildWindowsBootstrapScript(nextApp, bootstrapPaths), 'utf8');
  generated.windowsBootstrapPath = windowsBootstrapPath;

  const iosNotesPath = join(captureDir, 'ios-capture-notes.md');
  writeFileSync(iosNotesPath, buildIosNotes(nextApp, bootstrapPaths), 'utf8');
  generated.iosNotesPath = iosNotesPath;

  const androidProxyPath = join(captureDir, 'android-proxy-setup.sh');
  writeFileSync(androidProxyPath, buildAndroidProxyScript(nextApp, bootstrapPaths), 'utf8');
  generated.androidProxySetupPath = androidProxyPath;

  const androidClearProxyPath = join(captureDir, 'android-clear-proxy.sh');
  writeFileSync(androidClearProxyPath, buildAndroidClearProxyScript(nextApp), 'utf8');
  generated.androidClearProxyPath = androidClearProxyPath;

  const windowsProxyNotesPath = join(captureDir, 'windows-proxy-notes.md');
  writeFileSync(windowsProxyNotesPath, buildWindowsProxyNotes(), 'utf8');
  generated.windowsProxyNotesPath = windowsProxyNotesPath;

  return {
    app: nextApp,
    captureDir,
    generated,
  };
}

export class AppCaptureManager {
  constructor({ projectRoot = process.cwd(), assetStore = null } = {}) {
    this.projectRoot = projectRoot;
    this.assetStore = assetStore ?? new ReverseAssetStore({
      projectRoot,
      workflowName: 'tool-app-capture',
    });
    this.assetInitPromise = null;
    this.pendingAssetWrites = new Set();
    this.sessions = new Map();
  }

  async ensureAssetStore() {
    if (!this.assetInitPromise) {
      this.assetInitPromise = this.assetStore.init();
    }
    await this.assetInitPromise;
    return this.assetStore;
  }

  list() {
    return {
      count: this.sessions.size,
      items: [...this.sessions.values()].map((session) => this.#publicSession(session)),
    };
  }

  get(sessionId) {
    const session = this.sessions.get(sessionId);
    return session ? this.#publicSession(session) : null;
  }

  async start({ app = {}, options = {} } = {}) {
    const sessionId = randomUUID();
    const prepared = ensureGeneratedCaptureHelpers({
      projectRoot: this.projectRoot,
      sessionId,
      app,
    });
    const plan = buildNativeCapturePlan(prepared.app, options);
    const runnableSteps = toArray(plan.steps).filter((step) => step.enabled !== false);
    if (runnableSteps.length === 0) {
      throw new Error('no capture steps are enabled');
    }

    const session = {
      id: sessionId,
      createdAt: nowIso(),
      updatedAt: nowIso(),
      status: 'starting',
      app: prepared.app,
      captureDir: prepared.captureDir,
      generated: prepared.generated,
      plan,
      processes: [],
      logs: [],
      assetRef: null,
    };
    this.sessions.set(session.id, session);

    const manifestPath = join(prepared.captureDir, 'capture-manifest.json');
    const readmePath = join(prepared.captureDir, 'README.md');
    writeFileSync(readmePath, buildBundleReadme({
      appConfig: prepared.app,
      generated: {
        ...prepared.generated,
        captureManifestPath: manifestPath,
        bundleReadmePath: readmePath,
      },
      plan,
    }), 'utf8');
    session.generated.bundleReadmePath = readmePath;
    writeFileSync(manifestPath, JSON.stringify(buildCaptureManifest({
      sessionId,
      captureDir: prepared.captureDir,
      appConfig: prepared.app,
      generated: {
        ...prepared.generated,
        bundleReadmePath: readmePath,
        captureManifestPath: manifestPath,
      },
      plan,
    }), null, 2), 'utf8');
    session.generated.captureManifestPath = manifestPath;
    await this.#recordCaptureAsset(session);

    for (const step of runnableSteps) {
      const spec = resolveExecutionSpec(step, prepared.app);
      if (!spec.command) {
        ringPush(session.logs, {
          at: nowIso(),
          level: 'error',
          stepId: step.id,
          message: 'capture step has no runnable command',
        });
        session.status = 'failed';
        continue;
      }

      const child = spawn(spec.command, spec.args, {
        shell: spec.shell,
        windowsHide: true,
        stdio: ['ignore', 'pipe', 'pipe'],
      });
      const processInfo = {
        stepId: step.id,
        pid: child.pid ?? null,
        command: spec.command,
        args: spec.args,
        shell: spec.shell,
        startedAt: nowIso(),
        exitedAt: null,
        exitCode: null,
        signal: null,
      };
      const processEntry = { ...processInfo, child, exitPromise: null };
      session.processes.push(processEntry);
      ringPush(session.logs, {
        at: nowIso(),
        level: 'info',
        stepId: step.id,
        message: `started process ${spec.command}`,
      });

      child.stdout?.on('data', (chunk) => {
        ringPush(session.logs, {
          at: nowIso(),
          level: 'stdout',
          stepId: step.id,
          message: String(chunk).trim(),
        });
        session.updatedAt = nowIso();
      });
      child.stderr?.on('data', (chunk) => {
        ringPush(session.logs, {
          at: nowIso(),
          level: 'stderr',
          stepId: step.id,
          message: String(chunk).trim(),
        });
        session.updatedAt = nowIso();
      });
      processEntry.exitPromise = new Promise((resolve) => {
        child.on('exit', (code, signal) => {
          const target = session.processes.find((entry) => entry.stepId === step.id);
          if (target) {
            target.exitedAt = nowIso();
            target.exitCode = code;
            target.signal = signal;
            delete target.child;
          }
          ringPush(session.logs, {
            at: nowIso(),
            level: code === 0 ? 'info' : 'error',
            stepId: step.id,
            message: `process exited with code ${code ?? 'null'}${signal ? ` signal ${signal}` : ''}`,
          });
          const active = session.processes.some((entry) => entry.child);
          if (session.status !== 'stopped') {
            session.status = active ? 'running' : (code === 0 ? 'completed' : 'failed');
          }
          session.updatedAt = nowIso();
          void this.#recordCaptureAsset(session).catch(() => {});
          resolve({ code, signal });
        });
      });
    }

    if (session.status !== 'failed') {
      session.status = 'running';
    }
    return this.#publicSession(session);
  }

  async stop(sessionId) {
    const session = this.sessions.get(sessionId);
    if (!session) {
      return null;
    }

    session.status = 'stopped';
    session.updatedAt = nowIso();
    ringPush(session.logs, {
      at: nowIso(),
      level: 'info',
      message: 'capture session stopped',
    });

    const exitPromises = [];
    for (const entry of session.processes) {
      if (entry.child) {
        entry.child.kill();
      }
      if (entry.exitPromise) {
        exitPromises.push(entry.exitPromise);
      }
      entry.exitedAt ??= nowIso();
    }

    await Promise.allSettled(exitPromises);
    await this.#recordCaptureAsset(session);
    await this.#flushPendingAssetWrites();
    return this.#publicSession(session);
  }

  #publicSession(session) {
    return {
      id: session.id,
      createdAt: session.createdAt,
      updatedAt: session.updatedAt,
      status: session.status,
      captureDir: session.captureDir,
      generated: session.generated,
      assetRef: session.assetRef,
      plan: session.plan,
      processes: session.processes.map(({ child, exitPromise, ...entry }) => entry),
      logs: [...session.logs],
    };
  }

  async #recordCaptureAsset(session) {
    const writePromise = (async () => {
      const assetStore = await this.ensureAssetStore();
      const record = await assetStore.recordAppCapture(`tool-app-capture-${session.id}`, buildCaptureAssetPayload(session));
      session.assetRef = record;
      return record;
    })();

    this.pendingAssetWrites.add(writePromise);
    try {
      return await writePromise;
    } finally {
      this.pendingAssetWrites.delete(writePromise);
    }
  }

  async #flushPendingAssetWrites() {
    if (this.pendingAssetWrites.size === 0) {
      return;
    }
    await Promise.allSettled([...this.pendingAssetWrites]);
  }
}
